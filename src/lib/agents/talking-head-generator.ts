import type { FormatAgentInput } from "./types"
import { buildIdentitySection, buildAudienceSection } from "./prompt-helpers"

export function buildTalkingHeadPrompt({
  corePostText,
  coreIdentity,
  audienceIdentity,
}: FormatAgentInput): string {
  return `אתה תת-סוכן של סוכן פוסט הליבה. התפקיד שלך הוא לקחת פוסט ליבה קיים ולשכפל אותו לפורמט Talking Head — תסריט לדיבור למצלמה (רילס/טיקטוק/סטורי וידאו).

## הפוסט המקורי (פוסט ליבה)
${corePostText}

${buildIdentitySection(coreIdentity)}
${buildAudienceSection(audienceIdentity)}

## המשימה שלך
קח את פוסט הליבה והפוך אותו לתסריט דיבור למצלמה — טבעי, שיחתי, כאילו מדברים לחבר.

## כללי Talking Head — Best Practices
1. **פתיחה (הוק):** משפט פתיחה חזק שעוצר את הגלילה — שאלה, טענה מפתיעה, או פנייה ישירה לצופה
2. **גוף:** הרעיון המרכזי בשפה מדוברת. פסקאות קצרות, כאילו מדברים — לא כותבים
3. **סיום (CTA):** הנעה לפעולה ברורה — תגיבו, שתפו, עקבו, שלחו למישהו
4. אורך קריאה: 30-60 שניות (בערך 80-150 מילים)
5. שפה מדוברת — לא כתובה. קיצורים, ביטויים יומיומיים, כאילו מדברים לחבר
6. משפטים קצרים. הפסקות טבעיות. בלי משפטים מורכבים
7. שמור על הסגנון והטון של המשתמש — לא שפה גנרית
8. הרעיון המרכזי של הפוסט חייב לעבור בצורה ברורה

## פורמט הפלט
החזר את הטקסט בפורמט הזה בדיוק, בלי שום דבר נוסף:

[פתיחה]
טקסט הפתיחה

[גוף]
טקסט הגוף

[סיום]
טקסט הסיום`
}
