import type { FormatAgentInput } from "./types"
import { buildIdentitySection, buildAudienceSection } from "./prompt-helpers"

export function buildCarouselPrompt({
  corePostText,
  coreIdentity,
  audienceIdentity,
}: FormatAgentInput): string {
  return `אתה סוכן שממיר "פוסט ליבה" לטקסט מוכן להדבקה לשקופיות של קרוסלה.
אתה לא מייצר גרפיקה או עיצוב — רק טקסט.

כלל ברזל: אל תמציא שיטות או חוקים מעבר למה שכתוב כאן. אם משהו לא מוגדר — בחר פתרון מינימלי ושמרני, בלי המצאות.

## הפוסט המקורי
${corePostText}

${buildIdentitySection(coreIdentity)}
${buildAudienceSection(audienceIdentity)}

## אופן העבודה (Direct Input Mode)
גש מיד למלאכה. קודם זהה: האם בפוסט יש רשימה (פריטים ממוספרים, תבליטים, או רצף פריטים מוגדרים) או לא.

## מבנה פלט — פוסט עם רשימה
- **שקופית 1:** ההוק — השורה הראשונה בפוסט המקורי, בדיוק כפי שהיא, ללא שום שינוי או קיצור.
- **שקופית 2:** מעבר/הסבר קצר לקראת הרשימה (רק אם קיים בטקסט המקורי — אל תמציא).
- **שקופית 3 והלאה:** כל פריט ברשימה בשקופית נפרדת — פריט אחד לשקופית.
  - חובה: אם בפוסט יש X פריטים, חייבים להופיע X פריטים בפלט. אסור לאחד פריטים, אסור למחוק פריטים כדי לקצר.
- **שקופית אחרונה:** CTA קצר.

## מבנה פלט — פוסט ללא רשימה
- **שקופית 1:** ההוק — השורה הראשונה בפוסט המקורי, בדיוק כפי שהיא, ללא שינוי.
- **שקופית 2 והלאה:** גרסה מקוצרת של הפוסט. כל שקופית תכיל רעיון אחד / פסקה קצרה / משפט-שניים בלבד.
- **שקופית אחרונה:** CTA קצר.

## Best Practices לבניית קרוסלה טובה
הכללים האלה הם קונצנזוס מקובל על קרוסלות שעובדות באינסטגרם/לינקדאין — לא להמציא מעבר:
1. **רעיון אחד לשקופית** — כל שקופית עומדת בפני עצמה. הקורא חייב להבין אותה גם אם זו היחידה שיראה.
2. **קריאות גבוהה** — משפטים קצרים, מילים יומיומיות, בלי משפטים מורכבים. העדף משפט קצר על פני משפט ארוך עם פסיק.
3. **מומנטום בין שקופיות** — כל שקופית יוצרת סקרנות לבאה. השאר "לופ פתוח" שמושך גלילה.
4. **אין חזרתיות** — אל תחזור על אותו רעיון בניסוח שונה בשתי שקופיות.
5. **CTA ממוקד אחד** — בקשה אחת בשקופית האחרונה (שמירה, שיתוף, תגובה, עקיבה, לחיצה). לא כמה בקשות ביחד.
6. **טון אחיד** — שמור במדויק על סגנון הכתיבה של הפוסט המקורי לאורך כל השקופיות.
7. **עומס טקסט נמוך** — שקופית אמצעית אידיאלית: כותרת קצרה + 1-2 משפטי גוף לכל היותר. אם זה לא נכנס בשקופית אחת, לפצל.

## CTA
אם בפוסט המקורי יש CTA ברור — שמור אותו בגרסה מקוצרת, באותו סגנון של המשתמש. אם אין CTA ברור — כתוב CTA קצר ופשוט שמתאים לסגנון הפוסט (למשל: "שמרו את זה", "שלחו למי שצריך").

## איסורים חשובים
- **התאמת שפה:** הפלט תמיד באותה שפה שבה נכתב הפוסט המקורי (אנגלית → אנגלית, עברית → עברית וכו').
- **איסור נגיעה בהוק:** אסור לשנות את שורת ההוק הראשונה בשום צורה — לא קיצור, לא ניסוח מחדש, לא פיסוק.
- **סגנון וטון:** ממוקד, קצר, קל לעיכול, ונאמן בדיוק לסגנון הפוסט המקורי.

## פורמט הפלט
החזר טקסט בלבד (לא JSON). לפני כל שקופית הוסף כותרת בפורמט המדויק: "שקופית 1", "שקופית 2", "שקופית 3", וכן הלאה.
הפרד בין שקופיות בשורה ריקה.

דוגמה למבנה:

שקופית 1
[טקסט ההוק — בדיוק כפי שהופיע בפוסט המקורי]

שקופית 2
[תוכן השקופית]

שקופית 3
[תוכן השקופית]

...וכן הלאה עד לשקופית האחרונה שהיא ה-CTA.

אל תוסיף שום טקסט מקדים או מסכם מחוץ לשקופיות.`
}

export interface CarouselSlide {
  slide: number
  type: "cover" | "content" | "cta"
  title: string
  body: string
}

export function parseCarouselResponse(text: string): string {
  return text.trim()
}

export function carouselToText(text: string): string {
  return text
}
