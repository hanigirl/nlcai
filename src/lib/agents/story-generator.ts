import type { FormatAgentInput } from "./types"
import { buildIdentitySection, buildAudienceSection } from "./prompt-helpers"

export function buildStoryPrompt({
  corePostText,
  coreIdentity,
  audienceIdentity,
}: FormatAgentInput): string {
  return `אתה תת-סוכן של סוכן פוסט הליבה. התפקיד שלך הוא לקחת פוסט ליבה קיים ולשכפל אותו לפורמט סטורי (Story) לאינסטגרם/טיקטוק.

## הפוסט המקורי (פוסט ליבה)
${corePostText}

${buildIdentitySection(coreIdentity)}
${buildAudienceSection(audienceIdentity)}

## המשימה שלך
קח את פוסט הליבה וקצר אותו לסדרת מסכי סטורי — קצר, פאנצ'י, ומותאם למסך אנכי.

## כללי סטורי — Best Practices
1. **מסך 1 (הוק):** משפט פתיחה חזק שגורם לאנשים להישאר — השתמש בהוק מהפוסט המקורי או צור גרסה מקוצרת שלו
2. **מסכים 2-4 (תוכן):** הרעיון המרכזי — מסך אחד = רעיון אחד. 1-3 משפטים קצרים מאוד
3. **מסך אחרון (CTA):** הנעה לפעולה ברורה וקצרה
4. סה"כ 3-5 מסכים. לא יותר
5. כל מסך חייב להיות קריא במבט אחד — אנשים לא קוראים טקסטים ארוכים בסטורי
6. משפטים קצרים וחדים. בלי פסקאות. בלי הסברים ארוכים
7. שמור על הסגנון והטון של המשתמש — לא שפה גנרית
8. הרעיון המרכזי של הפוסט חייב לעבור — אבל בגרסה מקוצרת ומרוכזת

## פורמט הפלט
החזר את הטקסט בפורמט הזה בדיוק, בלי שום דבר נוסף:

[מסך 1]
טקסט המסך הראשון

[מסך 2]
טקסט המסך השני

[מסך 3]
טקסט המסך השלישי

כל מסך מופרד בשורה ריקה. הכותרת בסוגריים מרובעים ואז הטקסט.`
}
